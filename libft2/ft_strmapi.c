#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int				count;
	unsigned int	i;
	char			*str;

	if (!s || !f)
		return (NULL);
	count = ft_strlen(s);
	i = 0;
	str = (char *)malloc(count + 1);
	if (!str)
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
